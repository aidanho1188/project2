package edu.ho.java.midterm;

public class Rock {
	
	private int sample;
	private String rockType;
	private double weight;
	
	public Rock(int sample, double weight) {
		this.sample = sample;
		this.weight = weight;
	}

	public int getSample() {
		return sample;
	}

	public String getRockType() {
		return rockType;
	}

	public double getWeight() {
		return weight;
	}
	
	
	

}
